function payload = buildFixedWingPayload(target)
% buildFixedWingPayload - Снимок Payload самолёта с фиксированным крылом (FW2 и legacy).
payload = struct();
if ~isfield(target, 'Payload')
    return;
end
p = target.Payload;
isFW2 = isfield(target, 'Metadata') && isfield(target.Metadata, 'FW2') && target.Metadata.FW2;
payload.IsFW2 = isFW2;

if isFW2
    fields = {'RouteIndex', 'CurrentLegProgress', 'CurrentHeading', 'TargetHeading', ...
        'HeadingErrorDeg', 'TurnRateCommandDeg', 'CurrentSpeed', 'TargetSpeed', ...
        'BaseCruiseSpeed', 'SpeedProfileEvent', 'CurrentFlightLevel', 'FlightLevel', ...
        'TargetFlightLevel', 'AltitudeError', 'DesiredClimbRate', 'ClimbAngleDeg', ...
        'AltitudeProfileEvent', 'DistanceToBoundary', 'InWarningZone', 'InCriticalZone', ...
        'BorderFollowing', 'LastFW2Event', 'RouteComplete', 'HomePoint'};
else
    fields = {'CurrentHeading', 'TargetHeading', 'LoiterRadius', 'DiveTargetAltitude', ...
        'FlightLevel', 'TargetFlightLevel', 'AltitudeError', 'DesiredClimbRate', ...
        'ClimbAngleDeg', 'TurnSeverity', 'NavigationLookaheadPoint', 'CornerCuttingActive', ...
        'FinalPhase', 'FinalStrategy', 'FinalPhaseStarted', 'FinalMissionCompleted', ...
        'TimeInFinalPhase', 'DistanceToBoundary', 'NearBoundary', 'OutsideBoundary', ...
        'BoundaryRecoveryActive', 'RecoveryTarget', 'BoundaryRecoveryTarget', ...
        'RecoveryReason', 'LastBoundaryEvent', 'SafeZone', 'WarningZone', 'CriticalZone', ...
        'InSafeZone', 'InWarningZone', 'InCriticalZone', 'BorderFollowing', ...
        'BorderFollowingTime', 'BorderSide', 'NavigationMode', 'CurrentWaypointIndex', ...
        'NextWaypoint', 'NavigationTarget', 'LookaheadPoint', 'HeadingErrorDeg', ...
        'TurnRateCommandDeg', 'WaypointReached', 'LoiterActive', 'Action', ...
        'LastDecisionReason', 'HeadingJumpDeg', 'TargetPointJump', 'AntiBounceActive', ...
        'LastAntiBounceEvent', 'TimeOnCurrentLeg', 'RawNavigationTarget', ...
        'SmoothedNavigationTarget', 'RawLookaheadPoint', 'SmoothedLookaheadPoint', ...
        'CurrentWaypointIndex', 'DistanceToWaypoint', 'MissionComplete', 'HomePosition', ...
        'CurrentWaypoint', 'NoProgressTime', 'ForceDirectToWaypoint', ...
        'TotalXYExcursion', 'MaxAltitudeReached', 'MinAltitudeReached', 'LastNavigationEvent'};
end

for i = 1:numel(fields)
    if isfield(p, fields{i})
        payload.(fields{i}) = p.(fields{i});
    end
end
end
