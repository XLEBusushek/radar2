    'CurrentHeading', getOptionalNumericField(targetOut, 'CurrentHeading'), ...
    'TargetHeading', getOptionalNumericField(targetOut, 'TargetHeading'), ...
    'LoiterRadius', getOptionalNumericField(targetOut, 'LoiterRadius'), ...
    'DiveTargetAltitude', getOptionalNumericField(targetOut, 'DiveTargetAltitude'), ...
    'FlightLevel', getOptionalNumericField(targetOut, 'FlightLevel'), ...
    'TargetFlightLevel', getOptionalNumericField(targetOut, 'TargetFlightLevel'), ...
    'AltitudeError', getOptionalNumericField(targetOut, 'AltitudeError'), ...
    'DesiredClimbRate', getOptionalNumericField(targetOut, 'DesiredClimbRate'), ...
    'ClimbAngleDeg', getOptionalNumericField(targetOut, 'ClimbAngleDeg'), ...
    'TurnSeverity', getOptionalNumericField(targetOut, 'TurnSeverity'), ...
