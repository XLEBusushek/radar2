    'Waypoint', serializeVectorField(targetOut, 'Waypoint'), ...
    'SpeedLimit', getOptionalNumericField(targetOut, 'SpeedLimit'), ...
    'RoadDeviation', getOptionalNumericField(targetOut, 'RoadDeviation'), ...
    'RouteProgress', getOptionalNumericField(targetOut, 'RouteProgress'), ...
    'OnRoad', getOptionalLogicalField(targetOut, 'OnRoad', false), ...
    'IsOffRoad', getOptionalLogicalField(targetOut, 'IsOffRoad', false), ...
    'DriverProfile', string(getOptionalStringField(targetOut, 'DriverProfile')), ...
