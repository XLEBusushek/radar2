    'OutsideBoundary', getOptionalLogicalField(targetOut, 'OutsideBoundary', false), ...
    'BoundaryRecoveryActive', getOptionalLogicalField(targetOut, 'BoundaryRecoveryActive', false), ...
    'LastBoundaryEvent', string(getOptionalStringField(targetOut, 'LastBoundaryEvent')), ...
    'HeadingJumpDeg', getOptionalNumericField(targetOut, 'HeadingJumpDeg', 0), ...
    'TargetPointJump', getOptionalNumericField(targetOut, 'TargetPointJump', 0), ...
    'AntiBounceActive', getOptionalLogicalField(targetOut, 'AntiBounceActive', false), ...
    'LastAntiBounceEvent', string(getOptionalStringField(targetOut, 'LastAntiBounceEvent')), ...
    'TimeOnCurrentLeg', getOptionalNumericField(targetOut, 'TimeOnCurrentLeg', 0), ...
